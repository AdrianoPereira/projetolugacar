			e ganhar uma grana extra a pessoas que precisam de um carro por um determinado período seja ele horas, 
			dia ou semana. A ideia por trás da Lugcar é criar uma rede de compartilhamento de carros é facilitar 
			o acesso a um carro sem ter que adquirir um.</p>
